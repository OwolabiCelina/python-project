
grade: int = int(input("Enter a score: "))

if grade >= 70:
    print("Grade is A")
elif 60 <= grade <= 69:
    print("Grade is B")
elif 50 <= grade <= 59:
    print("Grade is C")
elif 45 <= grade <= 49:
    print("Grade is D")
elif 40 <= grade <= 44:
    print("Grade is E")
else:
    print("Grade is F")




